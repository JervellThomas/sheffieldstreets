            <p>Kid Acne is a street artist and hip-hop musician. Born in Malawi, he grew up in Leicestershire and eventually moved to Sheffield where he is currently based. His real name is Ed, and Ed still has acne according to his website, in case you are wondering where the name came from. Kid Acne is famous for his slogan murals and comical illustrations in many parts of the world. However, Ed’s most famous character is a tribe of nomadic matriarchal society known as Stabby Women. His work continues to be exhibited in museums in and out of Sheffield.</p>

<p>Stabby Women was originally intended for Sao Paulo, Brazil. Back in 2003, the tribe of women with swords started swooping Sao Paulo, expanding to New York, Munich, London, Barcelona, and of course Sheffield. The characters have a neutral experssion, often toppless carrying weaponry. According to Kid Acne, the character was created out of sheer need for strong female characters in the world of street art. Stabby Women is a reflection of his own upbringing by a strong single mother in Africa. In an interview with Our Favorite Places, Ed says he views Stabby Women as his substitute to the existing misogynistic characters in graffiti.</p>

<p>Kid Acne sees music and art as different mediums for essentially the same art. Thus, his collaboration with photographer Shaun Bloodworth for Stabby Women was relatively expected. The collaboration took the form of real women photographed in Sheffield as Stabby Women. In the wake of 3 headlining sexual assault allegations in Sheffield during the past year, one of which by a University of Sheffield student, and the virality of the #MeToo campaign, authentic representation is an act of power reclamation. It is an attempt to fight Big Bang Theory’s Mayim’s Balik shamefully blatant victim blaming in her New York Times article. Even going as far as claiming, her modest clothing and lack of effort to lose weight are her protection methods against sexual assault.</p>

<p>Perhaps, Terry Crews’ recent sharing of his own sexual assault is the definitive proof of the importance of characters like Stabby Women. The actor, a former athlete, is a startling 111 kgs and 1.91 m and yet that did not stop a powerful studio executive from publicly groping his private parts, in the presence of his wife. Simply put, the executive had more power than Crews in that setting. Sexual assault is a crime of power and an issue of representation. It’s about time images of real women are reflected to society. It’s about time pin-ups of Wonder Woman in her vomit-inducing skin tight bodysuit reflect aspirations of little girls to be strong, smart and determined. It’s about time the absurdity of blaming a piece of clothing for such heinous acts becomes commonly understood. It’s about time art, of any medium, reflects female ideals as opposed to the male oriented version of them. It’s about time art portrays female ownership of own body, till society catches up.</p>

